# Пригодятся функции
# sd.get_point()
# sd.snowflake()
# sd.sleep()
# sd.random_number()
# sd.user_want_exit()


def snowflakes(length):
    random_x = sd.random_number(0, 1000)
    y = 50
    while True:
        sd.clear_screen()

        point = sd.get_point(random_x, y)
        sd.snowflake(center=point, length=length)

        y -= 10


        if y < 50:
            break

        sd.sleep(0.1)
        if sd.user_want_exit():
            break


snowflakes(length=40)





sd.pause()




